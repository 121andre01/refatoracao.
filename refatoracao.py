class DriverFactory:
    """Factory para criaÃ§Ã£o e configuraÃ§Ã£o do WebDriver."""

    @staticmethod
    def create_chrome_driver(headless: bool = True) -> Optional[webdriver.Chrome]:
        """Cria instÃ¢ncia do Chrome Driver otimizada para Colab."""
        try:
            chrome_options = Options()

            if headless:
                chrome_options.add_argument("--headless")


            chrome_options.add_argument("--no-sandbox")
            chrome_options.add_argument("--disable-dev-shm-usage")
            chrome_options.add_argument("--disable-gpu")
            chrome_options.add_argument("--window-size=1920,1080")
            chrome_options.add_argument("--disable-web-security")
            chrome_options.add_argument("--allow-running-insecure-content")
            chrome_options.add_argument("--disable-extensions")
            chrome_options.add_argument("--disable-plugins")
            chrome_options.add_argument("--disable-images")
            chrome_options.add_experimental_option("excludeSwitches", ["enable-automation"])
            chrome_options.add_experimental_option('useAutomationExtension', False)

            driver = webdriver.Chrome(options=chrome_options)
            return driver

        except Exception as e:
            print(f"âŒ Erro ao criar driver: {e}")
            return None



