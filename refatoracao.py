    def execute_all_scenarios(self):
        """Executa todos os cenÃ¡rios de teste."""
        print("ğŸš€ INICIANDO AUTOMAÃ‡ÃƒO COMPLETA - DEMOBLAZE (Page Object Model)")
        print(f"â° InÃ­cio: {datetime.now().strftime('%Y-%m-%d %H:%M:%S')}")
